
public class SwapNumber{
    public static void main(String args[]){
    	
    	
    	int no1 = 25,no2 = 30;
    	System.out.println("*****Before swap*****");
    	System.out.println("first Number is: "+no1);
    	System.out.println("Second Number is: "+no2);

    	int temp = no1;
    	no1 = no2;
    	no2 = temp;
    	

    	System.out.println("*****after swap*****");
    	System.out.println("first Number is: "+no1);
    	System.out.println("Second Number is: "+no2);
    	
    }   

}